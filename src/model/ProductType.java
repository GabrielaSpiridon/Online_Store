package model;

/**
 * Enum-ul ProductType defineste tipurile principale de produse vandute in magazin.
 * Este folosit pentru a clasifica produsele (Cerinta 1).
 */
public enum ProductType {
        /**
         * Produse electronice (telefoane, laptopuri, accesorii).
         */
        ELECTRONIC,

        /**
         * Carti, manuale sau alte materiale de lectura.
         */
        BOOKS,

        /**
         * Articole de imbracaminte si incaltaminte.
         */
        CLOTHING
}